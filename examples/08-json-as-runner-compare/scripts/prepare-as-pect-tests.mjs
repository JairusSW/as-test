import fs from "node:fs/promises";
import path from "node:path";

const rootDir = process.cwd();
const sourceDir = path.join(rootDir, "assembly", "__tests__");
const outputDir = path.join(sourceDir, "as-pect");
const specSuffix = ".spec.ts";
const removeImportPattern = /^\s*import\s+\{[^}]*\b(?:describe|expect|it|test)\b[^}]*\}\s+from\s+"as-test";\s*\n/gm;
const importPattern = /(^\s*import\s+[^"']*from\s+)(["'])([^"']+)\2/gm;

async function listSpecFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  return entries.filter((entry) => entry.isFile() && entry.name.endsWith(specSuffix)).map((entry) => path.join(dir, entry.name)).sort();
}

function rewriteImports(source, fromFile, toFile) {
  return source.replace(importPattern, (full, prefix, quote, specifier) => {
    if (!specifier.startsWith(".")) return full;
    const originalTarget = path.resolve(path.dirname(fromFile), specifier);
    let next = path.relative(path.dirname(toFile), originalTarget).replaceAll(path.sep, "/");
    if (!next.startsWith(".")) next = "./" + next;
    return `${prefix}${quote}${next}${quote}`;
  });
}

async function main() {
  const specFiles = await listSpecFiles(sourceDir);
  await fs.rm(outputDir, { recursive: true, force: true, maxRetries: 5, retryDelay: 50 });
  await fs.mkdir(outputDir, { recursive: true });
  for (const specFile of specFiles) {
    const nextFile = path.join(outputDir, path.basename(specFile));
    const source = await fs.readFile(specFile, "utf8");
    const rewritten = rewriteImports(source.replace(removeImportPattern, ""), specFile, nextFile);
    await fs.writeFile(nextFile, rewritten);
  }
  await fs.writeFile(path.join(outputDir, "README.md"), "# Generated as-pect Tests\n\nGenerated by scripts/prepare-as-pect-tests.mjs.\n");
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
